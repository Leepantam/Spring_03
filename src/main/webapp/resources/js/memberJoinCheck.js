function check(){
	let all = document.getElementById("all");
	let ch = document.getElementsByClassName("check");
	let i = 0;
	for(i;i<ch.length;i++){
		if(ch[i].checked){

		} else{
			all.checked=false;
			break;
		}
	}
	if(i==ch.length){
		all.checked=true;
	}
}












function allCheck(){
	let all = document.getElementById("all");
	let ch = document.getElementsByClassName("check");
	for(let c of ch){
		c.checked = all.checked;
	}
}

